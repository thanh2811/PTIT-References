    public List<PBan> getNV(){
        List<PBan> l = new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            int ma = Integer.parseInt(tm.getValueAt(i, 0).toString());
            String ten=tm.getValueAt(i, 1).toString();
            String dc=tm.getValueAt(i, 2).toString();
//            String sdt=tm.getValueAt(i, 3).toString();
//            String ten=tm.getValueAt(i), 1).toString();
            int ml = Integer.parseInt(tm.getValueAt(i, 3).toString());
            l.add(new PBan(ma, ten, dc,  ml));
        }
        return l;
    }


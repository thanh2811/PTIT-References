    private void suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();
        if(r<0 || r>=tm.getRowCount()){
            JOptionPane.showMessageDialog(this,"Chon dong can sua");
        }
        else {
            jTextField1.requestFocus();
            s = true;
            buttonFalse();
        }
    }//GEN-LAST:event_suaActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        jTextField1.setText(tm.getValueAt(i, 0).toString());
        jTextField2.setText(tm.getValueAt(i, 1).toString());
        jTextField3.setText(tm.getValueAt(i, 2).toString());
        int indx = 0;
        for (int j = 0; j < jComboBox1.getComponentCount(); j++) {
            if(jComboBox1.getSelectedItem().toString().equalsIgnoreCase(tm.getValueAt(i, 3).toString())){
                indx = j;
                break;
            }
            
        }
        jComboBox1.setSelectedItem(indx);
    }//GEN-LAST:event_jTable1MouseClicked



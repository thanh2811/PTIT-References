    public List<MonHoc> getMH(){
        List<MonHoc> list = new ArrayList<>();
        for (int i = 0; i < tm.getRowCount(); i++) {
            int ma = Integer.parseInt(tm.getValueAt(i, 0).toString());
            String ten = tm.getValueAt(i, 1).toString();
            int sotiet = Integer.parseInt(tm.getValueAt(i, 2).toString());
            String loai = tm.getValueAt(i, 3).toString();
            list.add(new MonHoc(ma, ten, sotiet, loai));
        }
        return list;
       
    }
    
    private void themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_themActionPerformed
        // TODO add your handling code here:
        buttonFalse();
        List<MonHoc> l = getMH();
        t = true;
        if(l.size()>0)
            MonHoc.setSma(l.get(l.size()-1).getMa() + 1);
        
        jTextField1.setText(MonHoc.getSma() + "");
        jTextField2.requestFocus();
    }//GEN-LAST:event_themActionPerformed
    
    private void capnhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_capnhatActionPerformed
        // TODO add your handling code here:
        if(t){
            t = false;
            int ma = Integer.parseInt(jTextField1.getText()); 
            String ten = jTextField2.getText();
            int sotiet = Integer.parseInt(jTextField3.getText());
            String loai = jComboBox1.getSelectedItem().toString();
            MonHoc m = new MonHoc(ma,ten,sotiet,loai);
            tm.addRow(m.toObject());
        }
        if(s){
            s = false;
            int i = jTable1.getSelectedRow();
            tm.setValueAt(jTextField1.getText(), i, 0);
            tm.setValueAt(jTextField2.getText(), i, 1);
            tm.setValueAt(jTextField3.getText(), i, 2);
            tm.setValueAt(jComboBox1.getSelectedItem(), i, 3);
           
        }
        buttonTrue();
    }//GEN-LAST:event_capnhatActionPerformed

